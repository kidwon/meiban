      }
    }
    
    // 添加键盘导航支持
    window.addEventListener('keydown', this.handleKeyNavigation)
    
    // 初始化功能导航状态
    this.initializeFunctionNavigation()
  },
  
  beforeUnmount() {
    window.removeEventListener('resize', this.handleResize)
    window.removeEventListener('keydown', this.handleKeyNavigation)
  }
}
