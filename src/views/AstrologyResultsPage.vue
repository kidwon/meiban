      this.showTooltip = 'error';
      setTimeout(() => {
        this.showTooltip = null;
      }, 3000);

      // 可以显示用户友好的错误消息
      if (this.$toast) {
        this.$toast.error(this.$t('share.error') || '分享失败，请稍后重试');
      } else {
        alert(this.$t('share.error') || '分享失败，请稍后重试');
      }
    },

