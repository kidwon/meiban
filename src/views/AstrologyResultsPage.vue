    if (this.$route.meta.requiresSharedData) {
      // 分享页面数据由路由守卫处理，不需要在这里检查
      return;
    }
    
    if (!this.userData || !this.calculationResults) {
      this.$router.push({ name: 'home' })
    }
  },
  
